# Program to demonstrate different number data types in Python

# Integer
a = 20
print(a)
print(type(a))

# Float
b = 20.5
print(b)
print(type(b))

# Complex
c = 1j
print(c)
print(type(c))

# String
s = "GoEduHub.com"
print(s)
print(type(s))

# List
l = ["Go", "Edu", "Hub"]
print(l)
print(type(l))

# Tuple
t = ("Python", "from", "GoEduHub.com")
print(t)
print(type(t))

# Boolean
flag = True
print(flag)
print(type(flag))

# Bytes
by = b'Hello'
print(by)
print(type(by))