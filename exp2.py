# Program to demonstrate different number data types in Python

# Integer
a = 20
print(a)
print(type(a))

# Float
b = 20.5
print(b)
print(type(b))

# Complex
c = 69j
print(c)
print(type(c))

# String
s = "Github.com/rohith2608-eng"
print(s)
print(type(s))

# List
l = ["Git", "hub", "rohith2608-eng"]
print(l)
print(type(l))

# Tuple
t = ("Python", "from", "Github.com")
print(t)
print(type(t))

# Boolean
flag = True
print(flag)
print(type(flag))

# Bytes
by = b'Hello'
print(by)
print(type(by))